package com.task_management.taskmanagement.dto;

import lombok.Data;


public class GenreDTO {
    private Long id;

    public Long getId() {
        return id;
    }

    @Override
    public String toString() {
        return "GenreDTO{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    private String name;
}
